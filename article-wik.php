               <article>
                   <em>Judith décapitant Holopherne</em> est le titre de deux tableaux de l'artiste peintre baroque italienne Artemisia
                   Gentileschi, réalisés vers 1614-1620 conservés l'un au musée de Capodimonte à Naples et l'autre au musée
                   
                   <a href=""> des offices</a> à Florence.
                   
                   
               </article>
               <article>
               <h2> Thème</h2>
               <hr>
               L’œuvre met en scène Judith qui tranche la tête du
               général Holopherne. L’œuvre illustre une scène biblique
               devenue courante dans le monde de la peinture depuis la Renaissance,
               et qui fait partie d'un topos intitulé Pouvoir des femmes qui les montre dominant des hommes puissants. Tiré du livre de Judith des textes apocryphes 
               bibliques, le tableau s'intéresse au moment où Judith, assistée 
               de sa servante, décapite le général qui s'est endormi ivre.
               </article>
